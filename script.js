  // RESALTAR LA ORACIÓN Y CAMBIAR COLOR DE LA PALABRA EN EL CONTEXTO
  // --------------------------
  function highlightSentenceAndWord(text, occurrence) {
    const regex = /\S+/g;
    let match;
    let count = 0;
    let wordStart, wordEnd;
    while ((match = regex.exec(text)) !== null) {
      count++;
      if (count === occurrence) {
        wordStart = match.index;
        wordEnd = match.index + match[0].length;
        break;
      }
    }
    if (wordStart === undefined) {
      return text;
    }
    
    let sentenceStart = text.lastIndexOf('.', wordStart - 1);
    if (sentenceStart === -1) {
      sentenceStart = 0;
    } else {
      sentenceStart = sentenceStart + 1;
    }
    let sentenceEnd = text.indexOf('.', wordEnd);
    if (sentenceEnd === -1) {
      sentenceEnd = text.length;
    } else {
      sentenceEnd = sentenceEnd + 1;
    }
    
    const sentence = text.substring(sentenceStart, sentenceEnd);
    const offset = wordStart - sentenceStart;
    const wordPart = sentence.substring(offset, offset + (wordEnd - wordStart));
    
    // Resalta la palabra en rojo (solo cambia el color)
    const highlightedWord = `<span class="selected-word-context">${wordPart}</span>`;
    // Resalta la oración completa con fondo azul claro
    const highlightedSentence = `<span class="highlighted-sentence">${sentence.substring(0, offset)}${highlightedWord}${sentence.substring(offset + (wordEnd - wordStart))}</span>`;
    
    return text.substring(0, sentenceStart) + highlightedSentence + text.substring(sentenceEnd);
  }
  
